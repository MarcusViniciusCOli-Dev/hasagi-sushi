function abrirModal(){
    let modal = document.querySelector(".modal")
    let bg = document.querySelector(".bg")

    modal.style.display = 'block';
    bg.style.display = 'block';
}

function fechar(){
    let modal = document.querySelector(".modal")
    let bg = document.querySelector(".bg")

    modal.style.display = 'none';
    bg.style.display = 'none';
}

